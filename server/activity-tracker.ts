export async function autoTrackQuoteSent(params: AutoTrackQuoteParams): Promise<void> {
  const { customerId, quoteNumber, quoteId, totalAmount, itemCount, quoteItems, sentVia, userId, userName } = params;
  
  try {
    const now = new Date();
    const eventTitle = `Quote #${quoteNumber} sent via ${sentVia}`;
    const eventDescription = `Quote for $${parseFloat(totalAmount).toFixed(2)} with ${itemCount} items`;
    
    const activityEvent = await storage.createActivityEvent({
      customerId,
      eventType: 'quote_sent',
      title: eventTitle,
      description: eventDescription,
      sourceType: 'auto',
      sourceId: quoteId?.toString() || quoteNumber,
      sourceTable: 'sent_quotes',
      amount: totalAmount,
      itemCount,
      createdBy: userId,
      createdByName: userName,
      eventDate: now,
    });
    
    const config = await getConfigForEventType('quote_sent');
    if (config && config.isEnabled) {
      const dueDate = new Date(now);
      dueDate.setDate(dueDate.getDate() + (config.defaultDelayDays || 1));
      
      await storage.createFollowUpTask({
        customerId,
        title: config.taskTitle?.replace('{event}', `Quote #${quoteNumber}`) || `Follow up on Quote #${quoteNumber}`,
        description: config.taskDescription || `Check if customer received the quote and has any questions. Total: $${parseFloat(totalAmount).toFixed(2)}`,
        taskType: 'quote_follow_up',
        priority: config.defaultPriority || 'normal',
        status: 'pending',
        dueDate,
        sourceEventId: activityEvent.id,
        sourceType: 'quote',
        sourceId: quoteNumber,
        assignedTo: userId,
        assignedToName: userName,
        isAutoGenerated: true,
      });
    }
    
    if (quoteItems && quoteItems.length > 0) {
      for (const item of quoteItems) {
        await storage.createProductExposure({
          customerId,
          productId: item.productId || null,
          productName: item.productType || item.itemCode || 'Unknown Product',
          productCategory: item.category,
          exposureType: 'quote',
          sourceId: quoteNumber,
          sharedBy: userId,
          sharedByName: userName,
        });
      }
    }
    
    console.log(`✅ Auto-tracked quote ${quoteNumber} for customer ${customerId}`);
  } catch (error) {
    console.error('❌ Error auto-tracking quote:', error);
  }
}

