import { storage } from "./storage";
import type { InsertCustomerActivityEvent, InsertFollowUpTask, InsertProductExposureLog, FollowUpConfig } from "@shared/schema";

async function getConfigForEventType(eventType: string): Promise<FollowUpConfig | undefined> {
  const configs = await storage.getFollowUpConfig();
  return configs.find(c => c.eventType === eventType);
}

